function multiplicarArgumentos() {
   // Usa la palabra clave `arguments` para multiplicar todos los argumentos y devolver el producto.
   // Si no se pasan argumentos retorna 0. Si se pasa un argumento, simplemente retórnalo.
   // [PISTA]: "arguments" es un arreglo.
   // Tu código:
   var array_args = Array.from(arguments);

   if (array_args.length === 0) return 0;
   else if (array_args.length === 1) return array_args[0];
   else if (array_args.length >=2){
      var producto = array_args[0];
      for (let i=1; i< array_args.length; i++){
            producto = producto * array_args[i];
      }
      return producto;
   }   
}

