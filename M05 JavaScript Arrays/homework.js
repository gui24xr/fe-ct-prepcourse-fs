function breakStatement(num) {
   // Iterar en un bucle aumentando en 2 el número recibido hasta un límite de 10 veces.
   // Guardar cada nuevo valor en un arreglo y retornarlo.
   // Si en algún momento el valor de la suma y la cantidad de iteraciones coinciden, debe interrumpirse
   // la ejecución y retornar el string: "Se interrumpió la ejecución".
   // [PISTA]: utiliza el statement 'break'.
   // Tu código:
   var terminar = false; 
   var longitud_array  = 0; 
   var array_resultado = [];
   
   while (!terminar){ 
      num = num + 2;
      longitud_array = array_resultado.push(num);
      if (num === longitud_array) break;
      if (longitud_array === 10) terminar = true; 
   }
   
   if (num === longitud_array) return "Se interrumpió la ejecución";
   else return array_resultado;

}

